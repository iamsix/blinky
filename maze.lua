function Maze.draw(self)
    for i,row in ipairs(self.tilemap) do
        for j,tile in ipairs(row) do
            if tile ~= 0 then
                --Draw the image with the correct quad
                love.graphics.draw(self.map, 
                                   self.quads[tile], 
                                   j * self.tilewidth, 
                                   i * self.tileheight)
            end 
        end
    end
end